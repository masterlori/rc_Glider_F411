uint8_t crc8_calc(uint8_t init, uint8_t *buf, uint16_t len)
{
    uint8_t *d = (uint8_t *)buf;
    uint8_t tbl_idx;

    while (len--) {
        tbl_idx = init ^ *d;
        init = crc8_table[tbl_idx] & 0xff;
        d++;
    }

    return init;
}

