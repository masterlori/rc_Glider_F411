uint32_t crc32_calc (uint16_t init, uint32_t *buf, uint16_t len)
{
  uint32_t crc = init;
  while (len--)
    {
      crc = (crc << 8) ^ crc32_table[((crc >> 24) ^ *buf) & 255];
      buf++;
    }
  return crc;
}
